package unidades.unidad7.ejemplos.enumerados;

public class CartaNaipes {

	private PaloNaipes palo;
	private int puntos;
	
	public CartaNaipes(PaloNaipes palo, int puntos) {
		super();
		this.palo = palo;
		this.puntos = puntos;
	}

	public PaloNaipes getPalo() {
		return palo;
	}

	public int getPuntos() {
		return puntos;
	}
	
	
	
	
}
